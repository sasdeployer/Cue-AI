// cannedDeck builds a valid, presentable deck from the prompt without any LLM.
// It is used when ANTHROPIC_API_KEY is unset so the whole app is demoable.
func cannedDeck(prompt string) string {
	topic := cleanTopic(prompt)
	if topic == "" {
		topic = "Your Idea"
	}
	title := topic
	if len(title) > 48 {
		title = strings.TrimSpace(title[:48])
	}

	msg := "Here's a starter deck on " + topic + ". Add an ANTHROPIC_API_KEY to server/.env for fully AI-authored decks."

	app := strings.ReplaceAll(cannedAppTemplate, "{{TOPIC}}", topic)

	var b strings.Builder
	b.WriteString(msg)
	b.WriteString("\nTITLE: ")
	b.WriteString(title)
	b.WriteString("\n\n```css title=tokens.css\n")
	b.WriteString(tokensDefaultCSS)
	b.WriteString("\n```\n\n```tsx title=App.tsx\n")
	b.WriteString(app)
	b.WriteString("\n```\n")
	return b.String()
}

// cleanTopic keeps letters, numbers, spaces and light punctuation from the prompt.
func cleanTopic(s string) string {
	s = strings.TrimSpace(s)
	// drop a leading "create/build/make a deck about" style preamble
	lower := strings.ToLower(s)
	for _, p := range []string{"create a pitch deck for", "create a pitch deck about",
		"a pitch deck for", "a pitch deck about", "pitch deck for", "pitch deck about",
		"create a deck about", "build a deck about", "make a deck about",
		"a deck about", "deck about", "create a presentation about", "presentation about",
		"create a deck for", "build a deck for", "a deck for", "deck for",
		"pitch for", "pitch"} {
		if strings.HasPrefix(lower, p) {
			s = strings.TrimSpace(s[len(p):])
			break
		}
	}
	var out strings.Builder
	for _, r := range s {
		if unicode.IsLetter(r) || unicode.IsNumber(r) || unicode.IsSpace(r) ||
			strings.ContainsRune("-–—&.,:'", r) {
			out.WriteRune(r)
		}
	}
	res := strings.Join(strings.Fields(out.String()), " ")
	if res == "" {
		return res
	}
	// capitalize first letter for a title-ish feel
	r := []rune(res)
	r[0] = unicode.ToUpper(r[0])
	return string(r)
}

