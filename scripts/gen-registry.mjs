#!/usr/bin/env node
// Generates web/src/deck-runtime/registry.gen.ts — the pre-baked module surface the
// in-browser deck transpiler resolves imports against. The engine + component library
// are compiled ONCE into Cue's bundle; the generated App.tsx only ever imports from
// this fixed set. Run after the engine is synced into web/src/deck-template/src (dev.sh).
import { readdirSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const engineRel = 'web/src/deck-template/src'
const outFile = join(root, 'web/src/deck-runtime/registry.gen.ts')

/** @type {{ ident: string, importPath: string, specifier: string }[]} */
const modules = []
for (const group of ['deck', 'components']) {
  const dir = join(root, engineRel, group)
  for (const file of readdirSync(dir).sort()) {
    const m = file.match(/^(.+)\.tsx?$/)
    if (!m || file.endsWith('.d.ts')) continue
    const name = m[1]
    modules.push({
      ident: `_${group}_${name}`,
      importPath: `../deck-template/src/${group}/${name}`,
      specifier: `./${group}/${name}`,
    })
  }
}

const out = [
  '// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit by hand.',
  '// The fixed module surface the deck transpiler resolves imports against.',
  "import * as React from 'react'",
  "import * as ReactDOM from 'react-dom'",
  "import * as ReactDOMClient from 'react-dom/client'",
  "import * as FramerMotion from 'framer-motion'",
  ...modules.map((m) => `import * as ${m.ident} from '${m.importPath}'`),
  '',
  '// Normalize a module to a CJS-interop shape (Sucrase emits require()-based code).',
  'const wrap = (m: Record<string, unknown>) => ({ __esModule: true, ...m, default: m?.default ?? m })',
  '',
  'export const registry: Record<string, unknown> = {',
  '  react: wrap(React),',
  "  'react-dom': wrap(ReactDOM),",
  "  'react-dom/client': wrap(ReactDOMClient),",
  "  'framer-motion': wrap(FramerMotion),",
  ...modules.map((m) => `  '${m.specifier}': wrap(${m.ident}),`),
  '}',
  '',
].join('\n')

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, out)
console.log(`gen-registry: wrote ${modules.length + 4} modules to registry.gen.ts`)
